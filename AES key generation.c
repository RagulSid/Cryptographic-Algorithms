char hexa(char c)
{
     char a;
     if(c=='0')
          a=0x0;
     else if(c=='1')
          a=0x1;
     else if(c=='2')
          a=0x2;
     else if(c=='3')
          a=0x3;
     else if(c=='4')
          a=0x4;
     else if(c=='5')
          a=0x5;
     else if(c=='6')
          a=0x6;
     else if(c=='7')
          a=0x7;
     else if(c=='8')
          a=0x8;
     else if(c=='9')
          a=0x9;
     else if(c=='a' || c=='A')
          a=0xa;
     else if(c=='b' || c=='B')
          a=0xb;
     else if(c=='c' || c=='C')
          a=0xc;
     else if(c=='d' || c=='D')
          a=0xd;
     else if(c=='e' || c=='E')
          a=0xe;
     else if(c=='f' || c=='F')
          a=0xf;
     return a;
}

